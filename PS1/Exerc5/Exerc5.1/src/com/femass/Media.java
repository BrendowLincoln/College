package com.femass;

public class Media {

    public static void media(double n1, double n2) {

        System.out.println("A média entre 2 numeros é: " + (n1+n2)/2);
    }

    public static void media(double n1, double n2, double n3) {
        System.out.println("A média entre 3 numeros é: " + (n1+n2+n3)/3);
    }

    public static void media(double n1, double n2, double n3, double n4) {
        System.out.println("A média entre 3 numeros é: " + (n1+n2+n3+n4)/4);
    }
}
